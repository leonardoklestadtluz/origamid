// Retorne o url da página atual utilizando o objeto window
const urlAtual = window.location.href
console.log(`URL atual: ${urlAtual}`)

// Seleciona o primeiro elemento da página que possua a classe ativo
const elementoAtivo = document.querySelector('.ativo')
console.log(`Primeiro elemento: ${elementoAtivo}`)

// Retorne a linguagem do navegador
const idiomaNavegador = window.navigator.language
console.log(`Idioma do navegador: ${idiomaNavegador}`)

// Retorne a largura da janela 
const larguraJanela = window.innerWidth
console.log(`Largura da janela: ${larguraJanela}`)































